package ch01_start;

public class Hello {

	public static void main(String[] args) {
		//실행 단축키 [ Ctrl + F11 ]
		System.out.println("hello world!!");
		
		// 코드라인 복사 (Copy Lines)
		// 단축기 [Ctrl + Alt + 방향키 아래]
		System.out.println("오늘은 화요일"); 
		System.out.println("오늘은 화요일"); 

		// 코드라인 삭제 (Delete Line)
		// 단축기 [Ctrl + D]
		
		// 실행 취소의 취소 [Ctrl + Y]
		System.out.println("오늘은 화요일");
		System.out.println("오늘은 화요일");
		System.out.println("오늘은 화요일");
	}
}
